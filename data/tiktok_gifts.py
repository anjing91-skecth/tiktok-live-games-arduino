# Helper functions
def get_gifts_by_coin_range(min_coins=0, max_coins=999999):
    """Get gifts within a specific coin range"""
    return [gift for gift in TIKTOK_GIFTS if min_coins <= gift["coins"] <= max_coins]

def get_gift_by_name(name):
    """Get specific gift by name"""
    for gift in TIKTOK_GIFTS:
        if gift["name"].lower() == name.lower():
            return gift
    return None

def get_gift_by_coins(coins):
    """Get all gifts with exact coin value"""
    return [gift for gift in TIKTOK_GIFTS if gift["coins"] == coins]

def get_popular_gifts():
    """Get commonly used gifts"""
    popular_coins = [1, 5, 10, 99, 100, 199, 499, 999, 1000, 4999, 9999]
    popular = []
    for coins in popular_coins:
        gifts = get_gift_by_coins(coins)
        if gifts:
            popular.extend(gifts[:2])  # Max 2 per coin value
    return popular

